<template>
  <div class="col" :class="ColClass" :style="ColStyle">
    <slot></slot>
  </div>
</template>

<script>
let validator = value => {
  let flag = true;
  let keys = Object.keys(value);
  keys.forEach(key => {
    !["span", "offset"].includes(key) && (flag = false);
  });
  return flag;
};
export default {
  name: "GuluCol",
  data() {
    return {
      gutter: 0
    };
  },
  props: {
    span: {
      type: [Number, String]
    },
    offset: {
      type: [Number, String]
    },
    phone: {
      type: Object,
      validator
    },
    ipad: {
      type: Object,
      validator
    },
    narrowPc: {
      type: Object,
      validator
    },
    pc: {
      type: Object,
      validator
    },
    widePc: {
      type: Object,
      validator
    }
  },
  computed: {
    ColClass() {
      let { span, offset, phone, ipad, narrowPc, pc, widePc } = this;
      return [
        span && `col-${span}`,
        offset && `offset-${offset}`,
        phone && `col-phone-${phone.span}`,
        ipad && `col-ipad-${ipad.span}`,
        narrowPc && `col-narrow-pc-${narrowPc.span}`,
        pc && `col-pc-${pc.span}`,
        widePc && `col-wide-pc-${widePc.span}`
      ];
    },
    ColStyle() {
      let { gutter } = this;
      return {
        paddingLeft: gutter / 2 + "px",
        paddingRight: gutter / 2 + "px"
      };
    }
  }
};
</script>

